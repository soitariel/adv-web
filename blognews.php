  <h2>What We’ve Learnt From French Girls When It Comes To Style</h2>
  
   
<p>A lot has been written about the French girls’ style that some would say it is an ‘overdone’ topic, we disagree. From the pages of Vogue, Elle and Harpers Bazaar, to online blogs and fashion journals, much has been written on Parisian style do’s and don’ts for good reason, so much so that it’s got its own name – French chic. An understated and effortless sense of elegance and style, and we’re all for it. Best believe we’ve been taking notes.

Probably one of the biggest style icons to date (and actually the pioneer of ‘french chic’) was Mademoiselle Coco Chanel herself, whose work has stood the test of time and served inspiration all through Hollywood and the rest of the world. Her personal style introduced a more relaxed look, with well-tailored silhouettes, basics paired together in a simple casual look, and unique fabrics like tweed for the Chanel power suits the likes of Jackie Kennedy and Princess Diana infamously wore.

We don’t know it all but we have picked up a few tips from flipping through enough fashion magazines and our world wide web adventures on how to achieve that ever so elegant French chic style.
</p>
   
   
